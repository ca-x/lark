export type TKey = keyof (typeof dict)["zh-CN"];
export function createT(language: Language) {
  return (key: TKey) => dict[language]?.[key] ?? dict["zh-CN"][key];
}

const smartPlaylistKeys: Record<string, { title: TKey; hint: TKey }> = {
  "daily-mix": { title: "dailyMix", hint: "dailyMixHint" },
  "recently-played": { title: "recentPlayed", hint: "smartPlaylistRecentPlayedHint" },
  "recently-added": { title: "recentAdded", hint: "smartPlaylistRecentAddedHint" },
  favorites: { title: "favorites", hint: "smartPlaylistFavoritesHint" },
  unplayed: { title: "smartPlaylistUnplayed", hint: "smartPlaylistUnplayedHint" },
  "hi-res": { title: "smartPlaylistHiRes", hint: "smartPlaylistHiResHint" },
  "needs-lyrics": { title: "smartPlaylistNeedsLyrics", hint: "smartPlaylistNeedsLyricsHint" },
};

const libraryDirectoryStatusKeys: Record<string, TKey> = {
  online: "online",
  missing: "missing",
  not_directory: "notDirectory",
  unreadable: "unreadable",
};

export function smartPlaylistLabel(id: string, language: Language) {
  const t = createT(language);
  const keys = smartPlaylistKeys[id];
  return {
    title: keys ? t(keys.title) : id,
    hint: keys ? t(keys.hint) : "",
  };
}

export function libraryDirectoryStatusLabel(status: string, language: Language) {
  const t = createT(language);
  const key = libraryDirectoryStatusKeys[status] ?? "error";
  return t(key);
}
